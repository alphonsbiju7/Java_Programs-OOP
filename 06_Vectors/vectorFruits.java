import java.util.Vector;
public class vectorFruits {
    public static void main(String[] args) {
        Vector<String> fruits = new Vector<>();
        fruits.add("apple");
        fruits.add("Banana");
        fruits.add("Orange");
         fruits.add("Grapes");
        fruits.add("mango");

        for (String fruit : fruits) {
            System.out.println(fruit);

    }
    
}
}
